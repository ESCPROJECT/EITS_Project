public class Customers {
    private int customersAge;
    private int customersId;
    private String customersEmail;
    private String firstname;
    private String lastname;
    private String customerPassword;

    /**
     * @return the customersAge
     */
    public int getCustomersAge() {
        return customersAge;
    }

    /**
     * @param customersAge the customersAge to set
     */
    public void setCustomersAge(int customersAge) {
        this.customersAge = customersAge;
    }

    /**
     * @return the customersId
     */
    public int getCustomersId() {
        return customersId;
    }

    /**
     * @param customersId the customersId to set
     */
    public void setCustomersId(int customersId) {
        this.customersId = customersId;
    }

    /**
     * @return the customersEmail
     */
    public String getCustomersEmail() {
        return customersEmail;
    }

    /**
     * @param customersEmail the customersEmail to set
     */
    public void setCustomersEmail(String customersEmail) {
        this.customersEmail = customersEmail;
    }

    /**
     * @return the firstname
     */
    public String getFirstname() {
        return firstname;
    }

    /**
     * @param firstname the firstname to set
     */
    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    /**
     * @return the lastname
     */
    public String getLastname() {
        return lastname;
    }

    /**
     * @param lastname the lastname to set
     */
    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    /**
     * @return the customerPassword
     */
    public String getCustomerPassword() {
        return customerPassword;
    }

    /**
     * @param customerPassword the customerPassword to set
     */
    public void setCustomerPassword(String customerPassword) {
        this.customerPassword = customerPassword;
    }
}//end of Cutomers Class sets and gets: Christian
